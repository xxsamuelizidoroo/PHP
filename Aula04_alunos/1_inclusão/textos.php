<p>Carreira Musical: Bruno Mars é um cantor, compositor e produtor musical de renome internacional. Ele é conhecido por sua versatilidade musical e habilidade em misturar vários gêneros, incluindo pop, R&B, funk e soul. Sua carreira decolou na indústria da música no início dos anos 2010.</p>

<p>Nome Completo: O nome real de Bruno Mars é Peter Gene Hernandez.</p>

<p>Data de Nascimento: Bruno Mars nasceu em 8 de outubro de 1985, no Havaí, EUA.</p>

